export const foul = ( numOne = 0 , numTwo = 1) => {
    return (
        numOne + numTwo
    );
}

export const strike = ( numOne = 3 , numTwo = 0 ) => {
    return (
        numOne *  numTwo
    );
}

export const ball = ( numOne = 4, numTwo = 0 ) => {
    return (
        numOne * numTwo
    );
}

export const hit = ( numOne, numTwo = 0) => {
    return (
        numOne * numTwo
    );
}

